# Define strings
st1 = "Sonali"
st2 = "Sambare"

# Operations on strings
print("st1:", st1)
print("st2:", st2)

# Concatenation
print("st1 + st2 =", st1 + st2)

# Repetition
print("st2 * 2 =", st2 * 2)

# Swap case
print("st1.swapcase():", st1.swapcase())

# Splitting the string
print("st1.split():", st1.split())
print("st2.split('a'):", st2.split('a'))

# Finding substrings
print("st1.find('S'):", st1.find('S'))
print("st2.rfind('a'):", st2.rfind('a'))

# Max and Min characters
print("max(st2):", max(st2))
print("min(st2):", min(st2))

# Convert to lower and upper case
print("st1.lower():", st1.lower())
print("st2.upper():", st2.upper())

# Check if the string is alphabetic or numeric
print("st1.isalpha():", st1.isalpha())
print("st2.isdigit():", st2.isdigit())

# Capitalize the string
print("st1.capitalize():", st1.capitalize())

# Length of the string
print("Length of st1:", len(st1))

# Replace characters
print("st1.replace('a', 'J'):", st1.replace('a', 'J'))
